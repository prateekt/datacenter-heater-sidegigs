package com.heater.analysis;

import com.heater.acoustic.benchmark.AudioBenchmarkMetrics;

import java.util.List;
import java.util.Locale;

public final class MdmgBenchmarkExplainer {

    private MdmgBenchmarkExplainer() {}

    public static String explain(List<AudioBenchmarkMetrics.TierMetrics> tiers, double distillMse, String prompt) {
        StringBuilder sb = new StringBuilder();
        sb.append("## MDMG SOTA benchmark (Java-first pipeline)\n\n");
        sb.append("*Auto-generated by `./gradlew generateMdmgBenchmark`. Compares MDMG v1, MDMG v2, distilled landscape, and Java-LDM.*\n\n");
        sb.append("> **Concept doc:** [Mechanical Diffusion Model (MDM)](docs/mdmg_concept.md)\n\n");
        sb.append("**Prompt used:** \"").append(prompt).append("\"\n\n");

        sb.append("### Three-tier comparison\n\n");
        sb.append("| Tier | Spectral dist. | Harmonicity | CLAP proxy | FAD proxy |\n");
        sb.append("|------|----------------|-------------|------------|----------|\n");
        for (AudioBenchmarkMetrics.TierMetrics t : tiers) {
            sb.append("| **").append(t.tier()).append("** | ")
                    .append(fmt(t.spectralDistance())).append(" | ")
                    .append(fmt(t.harmonicity())).append(" | ")
                    .append(fmt(t.clapProxy())).append(" | ")
                    .append(fmt(t.fadProxy())).append(" |\n");
        }
        sb.append("\n");

        sb.append("### Distillation (Java-LDM → mechanical springs)\n\n");
        sb.append("| Metric | Value |\n|--------|-------|\n");
        sb.append("| Landscape distillation MSE | **").append(fmt(distillMse)).append("** |\n\n");

        sb.append("### Audio outputs\n\n");
        sb.append("| File | Description |\n|------|-------------|\n");
        sb.append("| [mdmg_v1_baseline.wav](docs/audio/mdmg_v1_baseline.wav) | Original MDMG v1 (fixed template) |\n");
        sb.append("| [mdmg_v2.wav](docs/audio/mdmg_v2.wav) | MDMG v2 STFT Langevin (default K) |\n");
        sb.append("| [mdmg_v2_distilled.wav](docs/audio/mdmg_v2_distilled.wav) | MDMG v2 with K distilled from Java-LDM |\n");
        sb.append("| [java_ldm_output.wav](docs/audio/java_ldm_output.wav) | Java-native latent diffusion (software ceiling) |\n\n");

        sb.append("### Software vs mechanical vs literature SOTA\n\n");
        sb.append("| Approach | What it is | This repo |\n");
        sb.append("|----------|------------|----------|\n");
        sb.append("| **Literature SOTA** | AudioLDM 2, Stable Audio Open, MusicGen — GPU-trained, billions of params | Cited for comparison; not ported |\n");
        sb.append("| **Java-LDM** | Task-specific latent diffusion in pure Java | Software GenAI viability demo |\n");
        sb.append("| **MDMG v2** | STFT Langevin with spring constants K | Mechanical inference cartoon |\n");
        sb.append("| **Distillation** | Fit K from Java-LDM score trajectories | Bridges software → mechanical |\n\n");

        sb.append("### Verdict\n\n");
        sb.append("> **Java-LDM** demonstrates that fan-noise→ambient transformation is **plausible in software** without Python at runtime. ")
                .append("**MDMG v2 distilled** asks whether spring networks can mimic that score landscape — distillation MSE above is the feasibility screen. ")
                .append("This is **not** AudioLDM 2 quality; it is a **DC-side-gig task** benchmark.\n\n");

        sb.append("### Gradle commands\n\n");
        sb.append("```bash\n");
        sb.append("./gradlew trainLatentDiffusion      # train Java score network\n");
        sb.append("./gradlew trainMdmgLandscape        # distill K into mdmg_landscape.yaml\n");
        sb.append("./gradlew generateMdmgBenchmark     # WAV clips + this table\n");
        sb.append("```\n");

        return sb.toString();
    }

    private static String fmt(double v) {
        return String.format(Locale.US, "%.3f", v);
    }
}
